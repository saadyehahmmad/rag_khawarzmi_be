@asynccontextmanager
async def _lifespan(app: FastAPI):
    """
    Validate optional vector-store strictness, log readiness, warm the graph.

    Warming loads embeddings once so the first user request is not a cold start.
    Registers a SIGTERM handler so long-running inference is allowed to drain before exit.
    """
    setup_observability()
    prom_init()  # Register Prometheus metrics at startup (no-op if library absent).

    def _handle_sigterm(*_: Any) -> None:
        logger.info("SIGTERM received вҖ” draining in-flight requests and shutting down")

    try:
        signal.signal(signal.SIGTERM, _handle_sigterm)
    except (OSError, ValueError):
        # SIGTERM not available on Windows or inside sub-threads; safe to skip.
        pass

    info = describe_vector_stores()
    strict = env_bool("RAG_REQUIRE_VECTOR_STORES", False)
    if strict and not all_vector_stores_ready():
        raise RuntimeError(
            "RAG_REQUIRE_VECTOR_STORES is enabled but one or more catalog vector collections are missing. "
            f"Details: {dumps(info)}"
        )
    if not all_vector_stores_ready():
        logger.warning(
            "Vector store readiness check failed вҖ” run ingestion before production. %s",
            dumps(info),
        )

    log_llm_startup_warnings()
    get_compiled_graph()
    # Embedding model is loaded lazily on the first request to avoid OOM on
    # memory-constrained hosts (e.g. Render free tier, 512 MB).
    # Remove this comment and call get_embeddings() here only on instances with вүҘ2 GB RAM.
    logger.info("API startup complete вҖ” ready to serve requests")
    yield
    logger.info("API shutdown complete")


